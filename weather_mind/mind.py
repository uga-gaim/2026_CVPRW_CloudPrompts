class Moondream2WeatherModel(BaseWeatherModel):
    """
    Vision-language WeatherMind model using vikhyatk/moondream2.

    - Takes an image (or path) + a text question/prompt.
    - Uses the model's built-in `query(image, question)` API.
    - Returns the raw text answer.
    """

    def __init__(
        self,
        model_id: str = "vikhyatk/moondream2",
        revision: str = "2025-06-21",
    ) -> None:
        self.model_id = model_id
        self.revision = revision

        try:
            import torch.backends.mps as mps_backend

            if hasattr(mps_backend, "is_available"):
                mps_backend.is_available = lambda: False
            if hasattr(mps_backend, "is_built"):
                mps_backend.is_built = lambda: False
        except Exception:
            pass

        self.device = torch.device("cpu")

        self.model = AutoModelForCausalLM.from_pretrained(
            self.model_id,
            revision=self.revision,
            trust_remote_code=True,
        )


    def __call__(self, input_data: Any, template: Optional[str] = None) -> str:
        """
        Run Moondream2 on (image, question) and return the answer string.
        """
        image = _load_image(input_data)

        question = template or DEFAULT_TEMPLATE

        result = self.model.query(image, question)

        answer = result.get("answer", "")
        return str(answer).strip()


