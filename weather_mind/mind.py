class Gemma3WeatherModel(BaseWeatherModel):
    """
    WeatherMind wrapper for Gemma 3 4B IT (multimodal).

    Uses:
        model_id = "google/gemma-3-4b-it"
        AutoProcessor.apply_chat_template for (image + question) -> answer.
    """

    def __init__(
        self,
        model_id: str = "google/gemma-3-4b-it",
        max_new_tokens: int = 500,
        temperature: float = 0.7,
        top_p: float = 0.9,
    ):
        self.model_id = model_id
        self.max_new_tokens = max_new_tokens
        self.temperature = temperature
        self.top_p = top_p

        if torch.backends.mps.is_available():
            self.device = torch.device("mps")
            dtype = torch.bfloat16
        else:
            self.device = torch.device("cpu")
            dtype = torch.float32

        self.processor = AutoProcessor.from_pretrained(self.model_id)

        self.model = Gemma3ForConditionalGeneration.from_pretrained(
            self.model_id,
            torch_dtype=dtype,
        ).to(self.device)
        self.model.eval()

    def __call__(self, input_data: Any, template: Optional[str] = None) -> str:
        """
        Args:
            input_data: image path or PIL.Image.Image
            template: the *question* about the image (string)
        Returns:
            Plain text answer from Gemma 3.
        """
        if template is None:
            template = DEFAULT_TEMPLATE

        image = _load_image(input_data)

        messages = [
            {
                "role": "system",
                "content": [
                    {
                        "type": "text",
                        "text": "You are WeatherMind, a vision-language assistant for understanding outdoor sky/weather images.",
                    }
                ],
            },
            {
                "role": "user",
                "content": [
                    {"type": "image", "image": image},
                    {"type": "text", "text": template},
                ],
            },
        ]

        inputs = self.processor.apply_chat_template(
            messages,
            tokenize=True,
            return_dict=True,
            return_tensors="pt",
            add_generation_prompt=True,
        ).to(self.device, dtype=self.model.dtype)

        input_len = inputs["input_ids"].shape[-1]

        with torch.inference_mode():
            generation = self.model.generate(
                **inputs,
                max_new_tokens=self.max_new_tokens,
                do_sample=True,
                temperature=self.temperature,
                top_p=self.top_p,
            )
            generation = generation[0, input_len:]

        decoded = self.processor.decode(
            generation,
            skip_special_tokens=True,
            clean_up_tokenization_spaces=True,
        )

        return decoded.strip()


