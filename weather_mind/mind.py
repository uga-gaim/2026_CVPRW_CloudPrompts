class BaseWeatherModel:
    """
    Base class for all WeatherMind models.

    Each concrete model must implement __call__(self, input_data: Any, template: str | None = None) -> Any
    so that it can be used as a callable.
    """

    def __call__(self, input_data: Any, template: Optional[str] = None) -> Any:
        raise NotImplementedError("Subclasses must implement __call__")


