############ QWEN #############
###############################

class QwenWeatherModel(BaseWeatherModel):
    """
    Concrete implementation of a Qwen2-VL model for WeatherMind.

    It:
      - Loads the Qwen2-VL-2B-Instruct vision-language model.
      - Accepts an image (or path) plus a text template/question.
      - Returns the raw text generated by the model.
    """

    def __init__(
        self,
        model_id: str = "Qwen/Qwen2-VL-2B-Instruct",
        max_new_tokens: int = 1000,
        temperature: float = 0.7,
        top_p: float = 0.9,
    ) -> None:
        self.model_id = model_id
        self.max_new_tokens = max_new_tokens
        self.temperature = temperature
        self.top_p = top_p

        self.device = torch.device("cpu")

        self.processor = AutoProcessor.from_pretrained(self.model_id)
        self.model = Qwen2VLForConditionalGeneration.from_pretrained(
            self.model_id,
            torch_dtype=torch.float32,
        ).to(self.device)

    def __call__(self, input_data: Any, template: Optional[str] = None) -> str:
        """
        Run Qwen2-VL on the given image + template and return the raw text output.

        Args:
            input_data: Image or path to image (PIL.Image.Image, str, or Path).
            template: Instruction/question about the image.
                      If None, uses DEFAULT_TEMPLATE.
        """
        image = _load_image(input_data)

        prompt = template or DEFAULT_TEMPLATE

        messages = [
            {
                "role": "user",
                "content": [
                    {"type": "text", "text": prompt},
                    {"type": "image", "image": image},
                ],
            }
        ]

        text = self.processor.apply_chat_template(
            messages,
            tokenize=False,
            add_generation_prompt=True,
        )

        inputs = self.processor(
            text=[text],
            images=[image],
            return_tensors="pt",
        ).to(self.device)

        with torch.inference_mode():
            generated_ids = self.model.generate(
                **inputs,
                max_new_tokens=self.max_new_tokens,
                do_sample=True,
                temperature=self.temperature,
                top_p=self.top_p,
            )

        output_ids = generated_ids[:, inputs["input_ids"].shape[1] :]

        text_out = self.processor.batch_decode(
            output_ids,
            skip_special_tokens=True,
        )[0]

        return text_out.strip()


